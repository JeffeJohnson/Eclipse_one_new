package org.onnee;

public class Student {
	
		private int id;
		private String name;
		private long phno;
		
		public  int getid(){
			return id;
		}
		public void setid(int id) {
			this.id=id;
		}
		public String getname() {
			return name;
		}
		public void setname(String name) {
			this.name=name;
		}
		public long getphno() {
			return phno;
		}
		public void setphno(long phno) {
			this.phno=phno;
		}
		
	}


